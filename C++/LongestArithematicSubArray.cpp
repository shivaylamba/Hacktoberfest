#include <bits/stdc++.h>
using namespace std;

int main()
{
    int a = 1, b = 2;
    for (int c = 116; c <= 118; c++)
    {
        if (0 || 1)
            a = a + c;
        else
            a = a + 1;
        if (0 && 1)
            a += c;
        else
            a += 1;
    }
    cout << a + b;
}
